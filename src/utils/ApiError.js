class ApiError extends Error{
    constructor(
         statusCode,
         message="Something is wrong",
         errors=[],
         stack = ''
    ){
        super(message);
        this.statusCode=statusCode;
        this.data = null;
        this.message=message;
        this.errors=errors;
        this.succcess=false;

        if(stack){
            this.stack=stack;
        }else{ 
            Error.captureStackTrace(this,this.constructor);
        }
        
    }
} 
export {ApiError};